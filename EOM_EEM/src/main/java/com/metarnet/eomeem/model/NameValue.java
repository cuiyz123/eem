package com.metarnet.eomeem.model;

public class NameValue {

    private String name;
    private String value;
    private String remark;
    private byte[] data;

    public byte[] getData() {
        return data;
    }

    public void setData(byte[] data) {
        this.data = data;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public NameValue(
            String name,
            String value) {
        this.name = name;
        this.value = value;
    }

    public NameValue(
            String name,
            String value,
            String remark,
            byte[] data) {
        this.name = name;
        this.value = value;
        this.remark = remark;
        this.data = data;
    }

    public NameValue() {
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }


}
